package dwz.business.stock;

import dwz.business.goods.Product;
import dwz.business.partner.Provider;
import dwz.persistence.BaseConditionVO;

public class SearchStockTakingVO extends BaseConditionVO {
	
	private Integer orgId;
	private Provider provider;
	private Product product;
	private String stockTakingNo;
	private String status;
	public Integer getOrgId() {
		return orgId;
	}
	public void setOrgId(Integer orgId) {
		this.orgId = orgId;
	}
	public Product getProduct() {
		return product;
	}
	public void setProduct(Product product) {
		this.product = product;
	}
	public String getStockTakingNo() {
		return stockTakingNo;
	}
	public void setStockTakingNo(String stockTakingNo) {
		this.stockTakingNo = stockTakingNo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Provider getProvider() {
		return provider;
	}
	public void setProvider(Provider provider) {
		this.provider = provider;
	}

}
