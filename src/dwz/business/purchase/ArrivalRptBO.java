package dwz.business.purchase;

public class ArrivalRptBO {
	
	private Integer no;
	private String productModel;
	private String productName;
	private String standard;
	private String unit;
	private Integer arrivalQuantity;
	private String inputPurchaseUnitPrice;
	private String inputArrivalPrice;
	private String pic;
	
	public Integer getNo() {
		return no;
	}
	public void setNo(Integer no) {
		this.no = no;
	}
	public String getProductModel() {
		return productModel;
	}
	public void setProductModel(String productModel) {
		this.productModel = productModel;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getStandard() {
		return standard;
	}
	public void setStandard(String standard) {
		this.standard = standard;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Integer getArrivalQuantity() {
		return arrivalQuantity;
	}
	public void setArrivalQuantity(Integer arrivalQuantity) {
		this.arrivalQuantity = arrivalQuantity;
	}
	public String getInputPurchaseUnitPrice() {
		return inputPurchaseUnitPrice;
	}
	public void setInputPurchaseUnitPrice(String inputPurchaseUnitPrice) {
		this.inputPurchaseUnitPrice = inputPurchaseUnitPrice;
	}
	public String getInputArrivalPrice() {
		return inputArrivalPrice;
	}
	public void setInputArrivalPrice(String inputArrivalPrice) {
		this.inputArrivalPrice = inputArrivalPrice;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}
	

}
