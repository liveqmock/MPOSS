package dwz.business.account;

public class BillBusiBO {
	
	private Integer targetId;
	private String targetName;
	private Integer totalBusiPrice;
	private Integer totalPayPrice;
	private Integer waitPayPrice;
	
	public Integer getTargetId() {
		return targetId;
	}
	public void setTargetId(Integer targetId) {
		this.targetId = targetId;
	}
	public String getTargetName() {
		return targetName;
	}
	public void setTargetName(String targetName) {
		this.targetName = targetName;
	}
	public Integer getTotalBusiPrice() {
		return totalBusiPrice;
	}
	public void setTotalBusiPrice(Integer totalBusiPrice) {
		this.totalBusiPrice = totalBusiPrice;
	}
	public Integer getTotalPayPrice() {
		return totalPayPrice;
	}
	public void setTotalPayPrice(Integer totalPayPrice) {
		this.totalPayPrice = totalPayPrice;
	}
	public Integer getWaitPayPrice() {
		return waitPayPrice;
	}
	public void setWaitPayPrice(Integer waitPayPrice) {
		this.waitPayPrice = waitPayPrice;
	}
	

}
