package dwz.business.goods;

import java.io.Serializable;

import dwz.framework.sys.business.AbstractBusinessObject;
import dwz.persistence.beans.ProProviderProduct;

public class ProviderProduct extends AbstractBusinessObject {
	
	private ProProviderProduct proProviderProduct;
	
	private Double inputUnitPrice;
	
	public ProviderProduct() {
		this.proProviderProduct = new ProProviderProduct();
	}
	
	public ProviderProduct(ProProviderProduct proProviderProduct) {
		this.proProviderProduct = proProviderProduct;
	}

	public ProProviderProduct getProProviderProduct() {
		return proProviderProduct;
	}

	public void setProProviderProduct(ProProviderProduct proProviderProduct) {
		this.proProviderProduct = proProviderProduct;
	}

	@Override
	public Serializable getId() {
		return this.proProviderProduct.getProviderProductId();
	}
	
	public Integer getProviderProductId() {
		return this.proProviderProduct.getProviderProductId();
	}
	public void setProviderProductId(Integer providerProductId) {
		this.proProviderProduct.setProviderProductId(providerProductId);
	}
	public Integer getOrgId() {
		return this.proProviderProduct.getOrgId();
	}
	public void setOrgId(Integer orgId) {
		this.proProviderProduct.setOrgId(orgId);
	}
	public Integer getProductId() {
		return this.proProviderProduct.getProductId();
	}
	public void setProductId(Integer productId) {
		this.proProviderProduct.setProductId(productId);
	}
	public Integer getProviderId() {
		return this.proProviderProduct.getProviderId();
	}
	public void setProviderId(Integer providerId) {
		this.proProviderProduct.setProviderId(providerId);
	}
	public String getProviderName(){
		return this.proProviderProduct.getProviderName();
	}
	public void setProviderName(String providerName){
		this.proProviderProduct.setProviderName(providerName);
	}
	public Integer getUnitPrice() {
		return this.proProviderProduct.getUnitPrice();
	}
	public void setUnitPrice(Integer unitPrice) {
		this.proProviderProduct.setUnitPrice(unitPrice);
	}
	public String getStandard() {
		return this.proProviderProduct.getStandard();
	}
	public void setStandard(String standard) {
		this.proProviderProduct.setStandard(standard);
	}
	public String getProductModel() {
		return this.proProviderProduct.getProductModel();
	}
	public void setProductModel(String productModel) {
		this.proProviderProduct.setProductModel(productModel);
	}
	public String getProductName() {
		return this.proProviderProduct.getProductName();
	}
	public void setProductName(String productName) {
		this.proProviderProduct.setProductName(productName);
	}
	public String getProductEngName() {
		return this.proProviderProduct.getProductEngName();
	}
	public void setProductEngName(String productEngName) {
		this.proProviderProduct.setProductEngName(productEngName);
	}
	public String getUnit() {
		return this.proProviderProduct.getUnit();
	}
	public void setUnit(String unit) {
		this.proProviderProduct.setUnit(unit);
	}
	public String getPic() {
		return this.proProviderProduct.getPic();
	}
	public void setPic(String pic) {
		this.proProviderProduct.setPic(pic);
	}
	public String getPicImg() {
		return "/styles/theme/img/product/"+this.proProviderProduct.getPic();
	}

	public Double getInputUnitPrice() {
		return inputUnitPrice;
	}

	public void setInputUnitPrice(Double inputUnitPrice) {
		this.inputUnitPrice = inputUnitPrice;
	}

}
